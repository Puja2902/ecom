
public class UserDetail {
	public String name = null;
	public long mobileNumber = 0;
	public String emailId = null;
	public String password = null;

	public UserDetail(String naam, long mob, String email, String pwd) {
		this.emailId= email;
		this.mobileNumber = mob;
		this.name = naam;
		this.password = pwd;
		
	
	}
}
